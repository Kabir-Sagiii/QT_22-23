function checkDay(day) {
  switch (day) {
    case "1":
      console.log("monday");
      break;
    case 2:
      console.log("Tuesday");

    case 3:
      console.log("Wednesday");

    case 4:
      console.log("Thursday");

    default:
      console.log("add valid input");
  }
}

checkDay(1);
